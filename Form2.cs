        private void LoadQuestion()
        {
            

            do
            {
                questionIndex = random.Next(0, questions.Count);
            } while (answeredQuestions.Contains(questionIndex));

            answeredQuestions.Add(questionIndex);
            questionBox.Text = questions[questionIndex];
            messageLabel.Text = "";
            for (int i = 0; i < 4; i++)
            {
                answerButtons[i].Text = answerOptions[questionIndex][i];
                answerButtons[i].Enabled = true;
            }

            nextButton.Enabled = false;
            if (!isLifelineUsed)
                lifelineButton.Enabled = true;
            timeRemaining = 30;
            questionTimer.Start();
        }

        private void AnswerButton_Click(object sender, EventArgs e)
        {
            var button = sender as Button;
            selectedAnswer = Array.IndexOf(answerButtons, button);
            messageLabel.Text = $"You selected: Option {selectedAnswer + 1}";
        }

        private void SubmitButton_Click(object sender, EventArgs e)
        {
            if (selectedAnswer == -1)
            {
                messageLabel.Text = "Please select an answer!";
                return;
            }

            if (selectedAnswer == correctAnswers[questionIndex])
            {
                // Correct answer logic
                currentScore = scoreMap[counter];
                scoreList.Items.Add($"Level {counter}: {scoreMap[counter]}");
                messageLabel.Text = "Correct! Move to the next level.";
                nextButton.Enabled = true; // Enable the "Next" button
                foreach (var button in answerButtons)
                    button.Enabled = false; // Disable buttons after correct answer
            }
            else
            {
                // Incorrect answer logic
                remainingChances--;
                messageLabel.Text = $"Incorrect! You have {remainingChances} chances left. Give it a try again!!";

                if (remainingChances == 0)
                {
                    MessageBox.Show("Game Over! You've use all chances :( ");
                    new Form3(currentScore).Show();
                    this.Close();
                    return;
                }

                // Re-enable the answer buttons for retry
                foreach (var button in answerButtons)
                    button.Enabled = true;

                nextButton.Enabled = false; // Prevent moving forward on a wrong answer
            }
        }


        private void NextButton_Click(object sender, EventArgs e)
        {
            if (selectedAnswer == correctAnswers[questionIndex])
            {
                counter++; // Increment the level counter ONLY on a correct answer
            }

            if (counter <= 10)
            {
                LoadQuestion(); // Load the next question or retry the same one
                selectedAnswer = -1; // Reset the selected answer
            }
            else
            {
                // End the game successfully
                MessageBox.Show($"Congratulations! You have completed the game!");
                new Form3(currentScore).Show();
                this.Close();
            }
        }
