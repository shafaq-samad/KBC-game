        private void QuestionTimer_Tick(object sender, EventArgs e)
        {
            timeRemaining--;
            timeLabel.Text = $"Time left: {timeRemaining}s";

            if (timeRemaining == 0)
            {
                questionTimer.Stop();
                timeLabel.Text = "Time's up! Moving to the next question.";
                nextButton.Enabled = true;
                foreach (var button in answerButtons)
                    button.Enabled = false; // Disable answer buttons when time's up
            }
        }
        private void LifelineButton_Click(object sender, EventArgs e)
        {
            if (isLifelineUsed)
            {
                messageLabel.Text = "You have already used the 50-50 lifeline!";
                return;
            }
            isLifelineUsed = true;
            lifelineButton.Enabled = false;
            List<int> incorrectOptions = new List<int>();
            for (int i = 0; i < 4; i++)
            {
                if (i != correctAnswers[questionIndex])
                {
                    incorrectOptions.Add(i);
                }
            }
            Random random = new Random();
            while (incorrectOptions.Count > 2)
            {
                incorrectOptions.RemoveAt(random.Next(incorrectOptions.Count));
            }
            foreach (int optionIndex in incorrectOptions)
            {
                answerButtons[optionIndex].Enabled = false;
            }
            messageLabel.Text = "Two incorrect options have been removed!";
        }
